INSERT INTO unit_models (manufacturer_id, unit_category_config_value_id, model_name, sort_order, is_active)
SELECT
  m.manufacturer_id,
  cv.config_value_id,
  seed.model_name,
  seed.sort_order,
  1
FROM tmp_unit_model_seed seed
INNER JOIN manufacturers m
  ON LOWER(REPLACE(REPLACE(m.name, ' ', ''), '-', '')) = LOWER(REPLACE(REPLACE(seed.manufacturer_name, ' ', ''), '-', ''))
INNER JOIN config_values cv
  ON LOWER(cv.code) = seed.category_code
INNER JOIN config_categories cc
  ON cc.config_category_id = cv.config_category_id
 AND cc.code IN ('unit_categories', 'unit_category', 'unit_types', 'unit_type')
LEFT JOIN unit_models existing
  ON existing.manufacturer_id = m.manufacturer_id
 AND existing.unit_category_config_value_id = cv.config_value_id
 AND LOWER(TRIM(existing.model_name)) = LOWER(TRIM(seed.model_name))
WHERE existing.unit_model_id IS NULL;

