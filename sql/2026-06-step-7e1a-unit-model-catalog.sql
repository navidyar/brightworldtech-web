-- Step 7e.1a: Manufacturer-Model Catalog
-- Provides a managed, manufacturer/category-scoped Unit Model catalog.

CREATE TABLE IF NOT EXISTS unit_models (
  unit_model_id INT NOT NULL AUTO_INCREMENT,
  manufacturer_id INT NOT NULL,
  unit_category_config_value_id INT NULL,
  model_name VARCHAR(150) NOT NULL,
  sort_order INT NOT NULL DEFAULT 0,
  is_active TINYINT(1) NOT NULL DEFAULT 1,
  PRIMARY KEY (unit_model_id),
  KEY idx_unit_models_catalog (manufacturer_id, unit_category_config_value_id, is_active, sort_order)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_unicode_ci;

-- Add catalog columns only where an older unit_models table does not already have them.
SET @has_manufacturer_id := (SELECT COUNT(*) FROM information_schema.COLUMNS WHERE TABLE_SCHEMA = DATABASE() AND TABLE_NAME = 'unit_models' AND COLUMN_NAME = 'manufacturer_id');
SET @sql := IF(@has_manufacturer_id = 0, 'ALTER TABLE unit_models ADD COLUMN manufacturer_id INT NULL', 'SELECT ''unit_models.manufacturer_id already exists'' AS message');
PREPARE stmt FROM @sql; EXECUTE stmt; DEALLOCATE PREPARE stmt;

SET @has_category_id := (SELECT COUNT(*) FROM information_schema.COLUMNS WHERE TABLE_SCHEMA = DATABASE() AND TABLE_NAME = 'unit_models' AND COLUMN_NAME = 'unit_category_config_value_id');
SET @sql := IF(@has_category_id = 0, 'ALTER TABLE unit_models ADD COLUMN unit_category_config_value_id INT NULL', 'SELECT ''unit_models.unit_category_config_value_id already exists'' AS message');
PREPARE stmt FROM @sql; EXECUTE stmt; DEALLOCATE PREPARE stmt;

SET @has_model_name := (SELECT COUNT(*) FROM information_schema.COLUMNS WHERE TABLE_SCHEMA = DATABASE() AND TABLE_NAME = 'unit_models' AND COLUMN_NAME = 'model_name');
SET @sql := IF(@has_model_name = 0, 'ALTER TABLE unit_models ADD COLUMN model_name VARCHAR(150) NULL', 'SELECT ''unit_models.model_name already exists'' AS message');
PREPARE stmt FROM @sql; EXECUTE stmt; DEALLOCATE PREPARE stmt;

SET @has_sort_order := (SELECT COUNT(*) FROM information_schema.COLUMNS WHERE TABLE_SCHEMA = DATABASE() AND TABLE_NAME = 'unit_models' AND COLUMN_NAME = 'sort_order');
SET @sql := IF(@has_sort_order = 0, 'ALTER TABLE unit_models ADD COLUMN sort_order INT NOT NULL DEFAULT 0', 'SELECT ''unit_models.sort_order already exists'' AS message');
PREPARE stmt FROM @sql; EXECUTE stmt; DEALLOCATE PREPARE stmt;

SET @has_is_active := (SELECT COUNT(*) FROM information_schema.COLUMNS WHERE TABLE_SCHEMA = DATABASE() AND TABLE_NAME = 'unit_models' AND COLUMN_NAME = 'is_active');
SET @sql := IF(@has_is_active = 0, 'ALTER TABLE unit_models ADD COLUMN is_active TINYINT(1) NOT NULL DEFAULT 1', 'SELECT ''unit_models.is_active already exists'' AS message');
PREPARE stmt FROM @sql; EXECUTE stmt; DEALLOCATE PREPARE stmt;

-- Preserve legacy display-name values if model_name was newly added.
SET @legacy_name_column := (
  SELECT COLUMN_NAME
  FROM information_schema.COLUMNS
  WHERE TABLE_SCHEMA = DATABASE()
    AND TABLE_NAME = 'unit_models'
    AND COLUMN_NAME IN ('name', 'label', 'model')
  ORDER BY FIELD(COLUMN_NAME, 'name', 'label', 'model')
  LIMIT 1
);
SET @sql := IF(
  @legacy_name_column IS NOT NULL,
  CONCAT('UPDATE unit_models SET model_name = COALESCE(NULLIF(model_name, ''''), `', @legacy_name_column, '`) WHERE model_name IS NULL OR model_name = '''''),
  'SELECT ''No legacy unit model name column to backfill'' AS message'
);
PREPARE stmt FROM @sql; EXECUTE stmt; DEALLOCATE PREPARE stmt;

-- Add core catalog manufacturers only when absent. The insert adapts to optional code/is_active columns.
SET @manufacturer_has_code := (SELECT COUNT(*) FROM information_schema.COLUMNS WHERE TABLE_SCHEMA = DATABASE() AND TABLE_NAME = 'manufacturers' AND COLUMN_NAME = 'code');
SET @manufacturer_has_is_active := (SELECT COUNT(*) FROM information_schema.COLUMNS WHERE TABLE_SCHEMA = DATABASE() AND TABLE_NAME = 'manufacturers' AND COLUMN_NAME = 'is_active');
SET @manufacturer_insert_columns := CONCAT('name', IF(@manufacturer_has_code > 0, ', code', ''), IF(@manufacturer_has_is_active > 0, ', is_active', ''));
SET @manufacturer_insert_values := CONCAT('seed.manufacturer_name', IF(@manufacturer_has_code > 0, ', LOWER(seed.manufacturer_name)', ''), IF(@manufacturer_has_is_active > 0, ', 1', ''));
SET @sql := CONCAT(
  'INSERT INTO manufacturers (', @manufacturer_insert_columns, ') ',
  'SELECT ', @manufacturer_insert_values, ' ',
  'FROM (SELECT ''Dell'' AS manufacturer_name UNION ALL SELECT ''HP'' UNION ALL SELECT ''Lenovo'' UNION ALL SELECT ''Apple'' UNION ALL SELECT ''Microsoft'') seed ',
  'LEFT JOIN manufacturers m ON LOWER(REPLACE(REPLACE(m.name, '' '', ''''), ''-'', '''')) = LOWER(REPLACE(REPLACE(seed.manufacturer_name, '' '', ''''), ''-'', '''')) ',
  'WHERE m.manufacturer_id IS NULL'
);
PREPARE stmt FROM @sql; EXECUTE stmt; DEALLOCATE PREPARE stmt;

